  case C: return &cpu->C;; case READ_C: return (word*)(cpu->Ram + cpu->C);; case NEXT_C: return (word*)(cpu->Ram + ((word*)cpu->Ram)[cpu->C + cpu->PC++]);;;
  case X: return &cpu->X;; case READ_X: return (word*)(cpu->Ram + cpu->X);; case NEXT_X: return (word*)(cpu->Ram + ((word*)cpu->Ram)[cpu->X + cpu->PC++]);;;
